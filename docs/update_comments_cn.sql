USE `lab-equipment-repair`;

ALTER TABLE `user`
  COMMENT = '系统用户表',
  MODIFY COLUMN `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT '主键',
  MODIFY COLUMN `username` VARCHAR(50) NOT NULL COMMENT '登录账号',
  MODIFY COLUMN `password_hash` VARCHAR(255) NOT NULL COMMENT '加密后的密码',
  MODIFY COLUMN `real_name` VARCHAR(50) NOT NULL COMMENT '真实姓名',
  MODIFY COLUMN `phone` VARCHAR(30) DEFAULT NULL COMMENT '联系电话',
  MODIFY COLUMN `role` VARCHAR(30) NOT NULL COMMENT '用户角色：ADMIN 系统管理员，LAB_MANAGER 实验室管理员，REPAIRER 维修人员，REPORTER 报修人',
