  MODIFY COLUMN `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间';

ALTER TABLE `repair_order`
  COMMENT = '报修工单主表',
  MODIFY COLUMN `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT '主键',
  MODIFY COLUMN `order_no` VARCHAR(50) NOT NULL COMMENT '工单编号',
  MODIFY COLUMN `equipment_id` BIGINT NOT NULL COMMENT '报修设备ID',
  MODIFY COLUMN `lab_id` BIGINT NOT NULL COMMENT '所属实验室ID，冗余保存便于查询和统计',
  MODIFY COLUMN `reporter_id` BIGINT NOT NULL COMMENT '报修人用户ID',
  MODIFY COLUMN `fault_description` TEXT NOT NULL COMMENT '故障描述',
  MODIFY COLUMN `urgency` VARCHAR(20) NOT NULL DEFAULT 'NORMAL' COMMENT '紧急程度：LOW 低，NORMAL 普通，HIGH 高，URGENT 紧急',
  MODIFY COLUMN `location` VARCHAR(150) DEFAULT NULL COMMENT '故障地点',
  MODIFY COLUMN `contact` VARCHAR(50) DEFAULT NULL COMMENT '报修人联系方式',
  MODIFY COLUMN `status` VARCHAR(30) NOT NULL DEFAULT 'SUBMITTED' COMMENT '工单状态：SUBMITTED 待审核，REJECTED 已退回，ASSIGNED 已派单，IN_PROGRESS 维修中，REPAIRED 待验收，ACCEPTED 已验收，CLOSED 已归档，CANCELLED 已取消',
  MODIFY COLUMN `assigned_to` BIGINT DEFAULT NULL COMMENT '指派维修人员用户ID',
  MODIFY COLUMN `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  MODIFY COLUMN `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间';

